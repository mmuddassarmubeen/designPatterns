    private void cmbDrugCompanyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbDrugCompanyActionPerformed
        // TODO add your handling code here:
        
        if(isLoaded)
        {
            Enterprise ent = (Enterprise)cmbDrugCompany.getSelectedItem();
            PharmaceuticalDrugRegulatoryDepartment dept = (PharmaceuticalDrugRegulatoryDepartment)ent.getDepartment(DepartmentType.PharmaceuticalDrugRegulatoryDepartment);
            DefaultListModel dlm = new DefaultListModel();
            for(Drug drug : dept.getDrugCatalog().getDrugList())
                {
                    dlm.addElement(drug);
                }
            listDrugs.setModel(dlm);
        }
    }//GEN-LAST:event_cmbDrugCompanyActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        this.pharmacyDepartment.getDrugCatalog().getDrugList().addAll( listDrugs.getSelectedValuesList());
        List<Drug> drugList = listDrugs.getSelectedValuesList();
        Enterprise ent = (Enterprise)cmbDrugCompany.getSelectedItem();
        PharmaceuticalDrugRegulatoryDepartment dept = (PharmaceuticalDrugRegulatoryDepartment)ent
                .getDepartment(DepartmentType.PharmaceuticalDrugRegulatoryDepartment);
        
        if(chkSubscribeToAlerts.isSelected())
        {
            for(Drug drug : drugList )
            {
                dept.getDrugUpdateSubscriptions().put(drug, this.pharmacyDepartment);
            }
        }
        JOptionPane.showMessageDialog(null,"Drugs added");
        this.populateDrugInventory();
    }//GEN-LAST:event_btnAddActionPerformed

