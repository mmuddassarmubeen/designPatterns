    private void populateDrugInventory()
    {
        DefaultTableModel model = (DefaultTableModel)tblDrugInventory.getModel();
        
            model.setRowCount(0);
        for(Drug drug : this.pharmacyDepartment.getDrugCatalog().getDrugList())
        {
            Object[] row = new Object[3];
            row[0] = drug.getName();
            row[1] = drug.getManufacturer();
            row[2] = drug.getDrugStatus();
            model.addRow(row);
        }
    }
    
    private void populatePharmaceuticalCompanies()
    {
        
        for(Enterprise ent : this.pharmaNetwork.getEnterpriseDirectory().getEnterpriseList())
        {
            cmbDrugCompany.addItem(ent);
        }
        cmbDrugCompany.setSelectedIndex(0);
        isLoaded = true;
    }
    
    
