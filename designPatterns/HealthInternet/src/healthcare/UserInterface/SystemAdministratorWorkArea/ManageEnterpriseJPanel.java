      private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) tableEnterprise.getModel();
                
        model.setRowCount(0);
        
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()){
                Object[] row = new Object[3];
                row[0] = enterprise.getName();
                row[1] = network.getName();
                row[2] = enterprise.getEnterpriseType().getValue();
                
                model.addRow(row);
            }
        
    }
    
    private void populateComboBox(){
       cmbNetwork.removeAllItems();
       cmbNetwork.addItem(network);
        isNetworkComboBoxLoaded = true;
    }
    
    private void populateSupportedEnterprisesTypes()
    {
        cmbEnterpriseType.removeAllItems();
        Network network = (Network)cmbNetwork.getSelectedItem();
        for (Enterprise.EnterpriseType type : network.supportedEnterpriseTypes()){
            cmbEnterpriseType.addItem(type);
        }
    }
    
