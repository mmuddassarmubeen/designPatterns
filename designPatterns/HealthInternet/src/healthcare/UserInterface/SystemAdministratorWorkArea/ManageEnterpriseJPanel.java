    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        if(!this.validateFields())
        {
            JOptionPane.showMessageDialog(null, "Please enter valid details");
            return;
        }
        if (cmbNetwork.getSelectedIndex() < 0  || cmbEnterpriseType.getSelectedIndex() < 0){
            JOptionPane.showMessageDialog(null, "Invalid Input!");
            return;
        }
        Network network = (Network) cmbNetwork.getSelectedItem();
        Enterprise.EnterpriseType type = (Enterprise.EnterpriseType)cmbEnterpriseType.getSelectedItem();
        String name = txtName.getText();
        Enterprise enterprise = network.getEnterpriseDirectory().createAndAddEnterprise(name, type);
        Logger logger = Logger.getLogger(this.getClass().toString());
        logger.log(Level.INFO,String.format("New Enterprise:%s of type:%s added",enterprise.getName(),type.getValue()));
        populateTable();

    }//GEN-LAST:event_btnSubmitActionPerformed

