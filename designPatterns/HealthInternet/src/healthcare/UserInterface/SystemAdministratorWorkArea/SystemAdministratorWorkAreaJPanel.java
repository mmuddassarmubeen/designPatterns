    private void populateTree() {

        DefaultTreeModel model = (DefaultTreeModel) JTree.getModel();

        ArrayList<Network> networkList = system.getNetworkList();
        Network network;
        
        DefaultMutableTreeNode networks = new DefaultMutableTreeNode(system);
        DefaultMutableTreeNode root = (DefaultMutableTreeNode) model.getRoot();
        root.insert(networks, 0);

        
        
        for (int i = 0; i < networkList.size(); i++) {
            network = networkList.get(i);
            
            
            this.recurseNetworkNodes(networks, network);
        }
        model.reload();
        
    }
    
    private void recurseNetworkNodes(DefaultMutableTreeNode parentNode, Network networkNode)
    {
        DefaultMutableTreeNode currentNode = new DefaultMutableTreeNode(networkNode);
        parentNode.add(currentNode);
        if(networkNode.getChildNetworks()==null)
        {
            DefaultMutableTreeNode enterpriseNode;
            DefaultMutableTreeNode organizationNode;
            ArrayList<Enterprise> enterpriseList;
            ArrayList<Department> organizationList;
            Enterprise enterprise;
            Department organization;
            enterpriseList = networkNode.getEnterpriseDirectory().getEnterpriseList();

                for (int j = 0; j < enterpriseList.size(); j++) 
                {
                    enterprise = enterpriseList.get(j);
                    enterpriseNode = new DefaultMutableTreeNode(enterprise);
                    currentNode.insert(enterpriseNode, j);

                    organizationList = enterprise.getDepartmentDirectory().getOrganizationList();
                        for (int k = 0; k < organizationList.size(); k++) 
                        {
                            organization = organizationList.get(k);
                            organizationNode = new DefaultMutableTreeNode(organization);
                            enterpriseNode.insert(organizationNode, k);
                        }
                }
        }
        else
        {
            for(Network net : networkNode.getChildNetworks())
            {
                recurseNetworkNodes(currentNode, net);
            }
        
        }
    
    }
        
