    private void JTreevalueChangedAction(javax.swing.event.TreeSelectionEvent evt) {//GEN-FIRST:event_JTreevalueChangedAction
        DefaultMutableTreeNode selectedNode = (DefaultMutableTreeNode) JTree.getLastSelectedPathComponent();
        if (selectedNode != null) {
            selectedNodeJLabel.setText(selectedNode.toString() + " " +selectedNode.getUserObject().getClass().toString());
        }
        if(selectedNode.getUserObject() instanceof EcoSystem 
                || selectedNode.getUserObject() instanceof CompositeNetwork)
        {
            btnManageRegionalNetwork.setEnabled(true);
            btnManageNetwork.setEnabled(true);
            btnManageEnterprise.setEnabled(false);
            btnManageEnterpriseAdmin.setEnabled(false);
        }
        if(selectedNode.getUserObject() instanceof HospitalNetwork
                || selectedNode.getUserObject() instanceof SocialNetwork
                || selectedNode.getUserObject() instanceof PharmaceuticalCompaniesNetwork)
        {
            btnManageRegionalNetwork.setEnabled(false);
            btnManageNetwork.setEnabled(false);
            btnManageEnterprise.setEnabled(true);
            btnManageEnterpriseAdmin.setEnabled(true);
            
        }
        if(selectedNode.getUserObject() instanceof Enterprise || selectedNode.getUserObject() instanceof Department)
        {
            btnManageRegionalNetwork.setEnabled(false);
            btnManageNetwork.setEnabled(false);
            btnManageEnterprise.setEnabled(false);
            btnManageEnterpriseAdmin.setEnabled(false);
            
        }
    }//GEN-LAST:event_JTreevalueChangedAction

