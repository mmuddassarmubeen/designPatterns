    private void populateTable()
    {
        DefaultTableModel model = (DefaultTableModel) tblAllTests.getModel();
        model.setRowCount(0);
        for(WorkRequest request : this.labdepartment.getWorkQueue().getWorkRequestList())
        {
            if(request instanceof MedicationWorkRequest && request.getStatus().equals(WorkRequestStatusType.Created))
            {
                Object[] row = new Object[3];
                Patient patient = ((MedicationWorkRequest)request).getPatient();
                row[0] = request; 
                row[1] = patient;
                row[2] = request.getSender();
                model.addRow(row);
            }
            
        }
    }
    
