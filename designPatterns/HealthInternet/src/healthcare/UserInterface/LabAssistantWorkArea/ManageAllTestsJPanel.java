    private void btnAssigntoMeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssigntoMeActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblAllTests.getSelectedRow();
        if(selectedRow < 0)
        {
	    JOptionPane.showMessageDialog(null,"Please select a row");
            return;
        }
        WorkRequest wr = (WorkRequest)tblAllTests.getValueAt(selectedRow, 0);
        wr.setReceiver(userAccount);
        wr.setStatus(WorkRequestStatusType.Processing);
        this.userAccount.getWorkQueue().getWorkRequestList().add(wr);
        Logger logger = Logger.getLogger(this.getClass().toString());
        logger.log(Level.INFO,String.format("Lab Test assigned to:%s ", this.userAccount.getPerson().getName()));
        this.populateTable();
    }//GEN-LAST:event_btnAssigntoMeActionPerformed

